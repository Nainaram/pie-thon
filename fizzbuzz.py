# this is the   simple program that print 1 to 100 numbers but at the
# mutiples of 3 and 5 it prints fizz and buzz
for num in range(1, 101):
    if num % 3 == 0 and num % 5 == 0:
        print("FizzBuzz")
    elif num % 3 == 0:
        print("Fizz")
    elif num % 5 == 0:
        print("Buzz")
    else:
        print(num)
